#include "fdf.h"

void	swap(t_node ***nd)
{
	int		buff;
	int		a;
	int		b;
	int		c;
	int		d;

	a = 0;
	b = nd[0][0]->h - 1;
	while (a < b)
	{
		c = 0;
		while (c < nd[0][0]->l)
		{
			buff = nd[a][c]->z;
			nd[a][c]->z = nd[b][c]->z;
			nd[b][c]->z = buff;
			c++;
		}
		a++;
		b--;
	}
}
