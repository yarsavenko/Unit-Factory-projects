#include "get_next_line.h"

int		fdf_atoi(char *str)
{
	int a;
	int b;
	int c;

	a = 0;
	c = 1;
	b = 0;
	while (str[a] <= 32)
		a++;
	if (str[a] == '-')
		c = -1;
	if (str[a] == '-' || str[a] == '+')
		a++;
	while (str[a] >= '0' && str[a] <= '9')
	{
		b = b * 10 + c * (str[a] - 48);
		a++;
	}
	return (b);
}
