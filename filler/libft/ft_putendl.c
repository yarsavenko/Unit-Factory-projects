#include "libft.h"
#include <unistd.h>

void	ft_putendl(char const *s)
{
	int a;

	a = 0;
	if (s)
	{
		while (s[a])
		{
			ft_putchar(s[a]);
			a++;
		}
		ft_putchar('\n');
	}
}
