#include "get_next_line.h"

void	multi_z(t_node ***asd, double m, int flag)
{
	int a;
	int b;

	a = 0;
	while (a < asd[0][0]->h)
	{
		b = 0;
		while (b < asd[0][0]->l)
		{
			if (flag == 2)
				asd[a][b]->z = (asd[a][b]->z / (m + 1)) * m;
			asd[a][b]->z *= m;
			b++;
		}
		a++;
	}
}
