#include "printf.h"

int			ft_atoi(char *str, int *i)
{
	int a;
	int b;

	a = 0;
	b = 0;
	while (str[a] <= 32)
		a++;
	while (str[a] >= 48 && str[a] <= 57)
		b = b * 10 + (str[a++] - 48);
	(*i) += a;
	return (b);
}
